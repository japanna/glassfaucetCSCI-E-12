<h3>2013 Best Bath Design Award</h3>
  <p>Glass Faucet artist and founder Jim McKelvey created the sculptural faucet and sink that were the centerpiece of the winning design at the 2013 NKBA Best Bath Awards.</p>
  <p>“I just couldn’t see using a metal faucet in the space, and needed something with more fluid lines. I couldn’t believe my luck when I found this faucet by Jim McKelvey," says interior designer Shea Pumarejo. “I love the way the light plays off the different textures of glass.”
